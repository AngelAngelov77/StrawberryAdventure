
namespace StrawberryAdventure
{
    using System;

    public static class ConsoleWriter
    {
        public static void Write(char text)
        {
            Console.Write(text);
        }
        public static void Write(string text)
        {
            Console.Write(text);
        }

        public static void WriteLine(string text)
        {
            Console.WriteLine(text);
        }
    }
}
